// Detect language: URL param > localStorage > browser > default
function detectLang() {
  const params = new URLSearchParams(window.location.search);
  if (params.has('lang') && TRANSLATIONS[params.get('lang')]) return params.get('lang');
  const saved = localStorage.getItem('bz_lang');
  if (saved && TRANSLATIONS[saved]) return saved;
  const nav = (navigator.language || '').slice(0, 2);
  if (TRANSLATIONS[nav]) return nav;
  return 'en';
}

let currentLang = detectLang();

function t(key) {
  return (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key]) || TRANSLATIONS.en[key] || key;
}

function setLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem('bz_lang', lang);
  document.documentElement.lang = lang;
  if (lang === 'ar') document.documentElement.dir = 'rtl';
  else document.documentElement.dir = 'ltr';
  // Re-translate all elements with data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  // Update lang picker display
  const picker = document.getElementById('lang-current');
  if (picker) picker.textContent = TRANSLATIONS[currentLang].lang_name;
}

// Language picker HTML (call from page init)
function renderLangPicker() {
  return `<div class="lang-picker">
    <button class="lang-btn" onclick="document.getElementById('lang-menu').classList.toggle('open')">
      <span id="lang-current">${TRANSLATIONS[currentLang].lang_name}</span> &#9662;
    </button>
    <div class="lang-menu" id="lang-menu">
      ${Object.keys(TRANSLATIONS).map(k => `<div class="lang-option${k===currentLang?' active':''}" onclick="setLang('${k}');document.getElementById('lang-menu').classList.remove('open')">${TRANSLATIONS[k].lang_name}</div>`).join('')}
    </div>
  </div>`;
}
